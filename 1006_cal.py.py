#2021.10.06 파이썬 계산기#

#변수 선언#
a=int(input("첫번째 숫자를 입력하세요"))#a= input(함수) 는 사용자로부터 값을 입력받은 후 a(변수)에 대입 ,=(연산자)
b=int(input("두번째 숫자를 입력하세요"))#b= input(함수) 는 사용자로부터 값을 입력받은 후 b(변수)에 대입 ,=(연산자)
# 산술연산자를 통한 계산법 #
result= a+b# result= 그릇 #
print(a,"+",b,"=",result)  # print =  출력 #
result= a-b
print(a,"-",b,"=",result)
result= a*b
print(a,"*",b,"=",result)
result= a/b
print(a,"/",b,"=",result)


